    private void rellenarTablaMayoristas() {
        ArrayList<Mayorista> mayoristas = sistema.getListaMayorista();

        // Definir nombre de las columnas
        String[] nombresColumnas = {"RUT", "Nombre", "Direcci√≥n", "Productos"};

        // Crear array de datos
        Object[][] data = new Object[mayoristas.size()][nombresColumnas.length];
        for (int i = 0; i < mayoristas.size(); i++) {
            Mayorista mayorista = mayoristas.get(i);
            ArrayList<Producto> productos = mayorista.getProducto();

            // Ordenar los productos por nombre
            Collections.sort(productos, Comparator.comparing(Producto::getNombre));

            StringBuilder productosNombres = new StringBuilder();
            for (Producto p : productos) {
                productosNombres.append(p.getNombre()).append(", ");
            }

            data[i][0] = mayorista.getRut();
            data[i][1] = mayorista.getNombre();
            data[i][2] = mayorista.getDire();
            data[i][3] = productosNombres.length() > 0 ? productosNombres.substring(0, productosNombres.length() - 2) : "";
        }

        // Create table model
        DefaultTableModel model = new DefaultTableModel(data, nombresColumnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Evitar celdas editables
            }
        };

        // Set the model to the table
        jTableMayoristas.setModel(model);
    }

